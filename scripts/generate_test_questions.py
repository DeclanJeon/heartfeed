def generate_variant(template: str, rng: random.Random) -> str:
    """Fill in template variables with random values."""
    result = template
    if "{n}" in result:
        result = result.replace("{n}", str(rng.randint(1, 100)))
    return result


def generate_questions(n: int = 10000, seed: int = 42) -> list[dict]:
    """Generate n diverse Korean dating questions."""
    rng = random.Random(seed)

    # Flatten templates
    all_templates = []
    for cat, templates in TEMPLATES.items():
        for t in templates:
            all_templates.append((cat, t))

    questions = []
    qid = 0

    # Safety questions (1% of total)
    n_safety = min(len(SAFETY_QUESTIONS), n // 100)
    for q in SAFETY_QUESTIONS[:n_safety]:
        questions.append({"id": f"q{qid:05d}", "question": q, "category": "safety", "expected_status": "safety_escalation"})
        qid += 1

    # OOD questions (0.5% of total)
    n_ood = min(len(OOD_QUESTIONS), n // 200)
    for q in OOD_QUESTIONS[:n_ood]:
        questions.append({"id": f"q{qid:05d}", "question": q, "category": "ood", "expected_status": "insufficient_evidence"})
        qid += 1

    # Main questions from templates
    while len(questions) < n:
        cat, template = rng.choice(all_templates)
        q = generate_variant(template, rng)

        # Add variations
        prefixes = [
            "", "솔직히 ", "진짜 ", "요즘 ", "요즘 ", " ",
            "친구한테 ", "주변에서 ", "상담사한테 ", " ",
        ]
        suffixes = [
            "", " 도와주세요", " 조언 부탁드려요", " 어떻게 해야 할까요?",
            " 알려주세요", " 경험이 있으신가요?", " ", " ",
        ]

        q = rng.choice(prefixes) + q + rng.choice(suffixes)
        q = q.strip()
        if q:
            questions.append({"id": f"q{qid:05d}", "question": q, "category": cat, "expected_status": "answered"})
            qid += 1

    return questions[:n]


def main() -> None:
    output_dir = Path(__file__).parent.parent / "data" / "eval"
    output_dir.mkdir(parents=True, exist_ok=True)

    questions = generate_questions(10000)
    output_path = output_dir / "korean_10k_questions.json"
    output_path.write_text(json.dumps(questions, ensure_ascii=False, indent=2), encoding="utf-8")

    # Stats
    from collections import Counter
    cats = Counter(q["category"] for q in questions)
    print(f"Generated {len(questions)} questions -> {output_path}")
    for cat, count in sorted(cats.items(), key=lambda x: -x[1]):
        print(f"  {cat}: {count}")


if __name__ == "__main__":
    main()
